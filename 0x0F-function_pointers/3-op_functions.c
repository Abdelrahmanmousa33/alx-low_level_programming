#include "3-calc.h"
/**
 * op_add - add
 * @a:num1
 * @b:num2
 * Return:result of the operation
 */
int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtract
 * @a:num1
 * @b:num2
 * Return:result of the operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multply
 * @a:num1
 * @b:num2
 * Return:result of the operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a:num1
 * @b:num2
 * Return:result of the operation
 */
int op_div(int a, int b)
{
	return (a / b);

}
/**
 * op_mod- modules
 * @a:num1
 * @b:num2
 * Return:result of the operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
